'''Você encontrou uma varinha mágica que pode dobrar qualquer coisa! Mas espere... Isso funcionaria com números? Vamos tentar! Peça ao usuário para inserir um número. Em seguida, calcule o dobro desse número e imprima o resultado.'''
# n1 recebe o valor inteiro
n1 = int(input("Digite um número que você deseja saber o dobro:"))
# multi2 recebe o valor de n1 multiplicado por 2
multi2 = n1 * 2
# imprimi o valor de multi2
print(f'O dobro de {n1} é igual a {multi2}')
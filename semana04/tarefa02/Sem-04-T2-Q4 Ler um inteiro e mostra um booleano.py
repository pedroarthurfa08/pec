'''Escreva um programa que leia um valor inteiro e mostra na tela no valor booleano True caso o número lido seja maior que 100 ou False caso contrário.'''
# n recebe um valor inteiro
n = int(input("Insira um número: "))
# n deve ser um número maior que 100
n > 100
# se n for maior ou igual a 100 será imprimido true caso não será imprimido false
print(n >= 100)